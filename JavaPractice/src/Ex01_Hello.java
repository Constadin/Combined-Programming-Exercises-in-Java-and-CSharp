// Define a public class named Ex01_Hello
public class Ex01_Hello {

    // The main method, which is the entry point of the program
    public static void main(String[] args){
        // Print "Hello Java!" followed by a tab escape sequence (\t)
        System.out.println("Hello Java!\t");

        // Print "I am developer" without starting a new line
        System.out.print("I am developer");

        // Print "print (no break Line)" without starting a new line
        System.out.print("print (no break Line)");
    }
}

/*Explanation:
Class Declaration:

public class Ex01_Hello {: Defines a public class named Ex01_Hello.
Main Method:

public static void main(String[] args) {: This is the main method, which serves as the entry point for the program.

Printing Statements:
System.out.println("Hello Java!\t");: Prints "Hello Java!" to the console, followed by a tab character (\t),
and then moves to the next line.

System.out.print("I am developer");: Prints "I am developer" to the console without starting a new line.

System.out.print("print (no break Line)");: Prints "print (no break Line)" to the console without starting a new line.

Notes:
System.out.println() is used to print a line of text to the console and then move to the next line.
System.out.print() is used to print text to the console without moving to the next line, so subsequent outputs
continue on the same line.
This Java program demonstrates basic output operations using System.out.println() and System.out.print(),
showing how text can be displayed with or without line breaks depending on which method is use*/

